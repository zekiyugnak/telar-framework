#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');

// Parse the `## Work Units` section of active-plan.md into structured WUs.
// Each WU is a `### WU-xxx:` block; within it we read the `file_scope:` and
// `deps:` list/inline fields. Unknown fields (spec, dod, checkpoint) are ignored.
function parsePlan(planText, isolateFileScope = false) {
  const lines = planText.split('\n');
  const wus = [];
  let current = null;
  let listKey = null; // 'file_scope' | 'deps' while consuming a multiline list

  const headerRe = /^###\s+(WU-[A-Za-z0-9_-]+):/;
  const inlineDepsRe = /^-\s*deps:\s*\[([^\]]*)\]\s*$/;
  const listStartRe = /^-\s*(file_scope|deps):\s*$/;
  const listItemRe = /^\s+-\s+(.+?)\s*$/;

  const pushCurrent = () => {
    if (current) wus.push(current);
  };

  for (const line of lines) {
    const h = line.match(headerRe);
    if (h) {
      pushCurrent();
      current = { id: h[1], deps: [], fileScope: [] };
      listKey = null;
      continue;
    }
    if (!current) continue;

    const inlineDeps = line.match(inlineDepsRe);
    if (inlineDeps) {
      current.deps = inlineDeps[1]
        .split(',')
        .map((s) => s.trim())
        .filter(Boolean);
      listKey = null;
      continue;
    }

    const ls = line.match(listStartRe);
    if (ls) {
      listKey = ls[1];
      continue;
    }

    if (listKey) {
      const item = line.match(listItemRe);
      if (item) {
        const val = item[1].trim();
        if (listKey === 'file_scope') current.fileScope.push(val);
        else current.deps.push(val);
        continue;
      }
      // A non-indented / non-list line ends the current list.
      listKey = null;
    }
  }
  pushCurrent();

  return { wus, warnings: computePlanWarnings(wus, isolateFileScope) };
}

// Static conflict check (Bazel "output generated by 2 actions"): two WUs with
// no dep edge between them that both declare the same path is an ambiguous plan.
//
// Under worktree isolation, overlapping file_scope is INTENTIONAL — each WU
// edits its own checkout and the orchestrator merges back with conflict
// detection (see orchestrated-execution Phase 4). So the ambiguous-plan warning
// is suppressed; genuine conflicts surface at merge-back time, not here.
function computePlanWarnings(wus, isolateFileScope = false) {
  const warnings = [];
  if (isolateFileScope) return warnings;
  const depReachable = buildReachability(wus);
  for (let i = 0; i < wus.length; i++) {
    for (let j = i + 1; j < wus.length; j++) {
      const a = wus[i];
      const b = wus[j];
      const ordered = depReachable(a.id, b.id) || depReachable(b.id, a.id);
      if (ordered) continue;
      const shared = a.fileScope.filter((p) => b.fileScope.includes(p));
      if (shared.length > 0) {
        warnings.push(
          `${a.id} and ${b.id} both declare ${shared.join(', ')} with no dep ordering — ` +
            'ambiguous plan; add a dep edge or split the shared file.'
        );
      }
    }
  }
  return warnings;
}

// Returns a predicate reachable(from, to): is `to` a transitive dependency of `from`?
function buildReachability(wus) {
  const deps = new Map(wus.map((w) => [w.id, w.deps]));
  return function reachable(from, to) {
    const seen = new Set();
    const stack = [...(deps.get(from) || [])];
    while (stack.length) {
      const cur = stack.pop();
      if (cur === to) return true;
      if (seen.has(cur)) continue;
      seen.add(cur);
      stack.push(...(deps.get(cur) || []));
    }
    return false;
  };
}

// Parse the `## Work Unit Status` markdown table into id -> status.
function parseState(stateText) {
  const map = new Map();
  const rowRe = /^\|\s*(WU-[A-Za-z0-9_-]+)\s*\|\s*([A-Za-z-]+)\s*\|/;
  for (const line of stateText.split('\n')) {
    const m = line.match(rowRe);
    if (m) map.set(m[1], m[2].toUpperCase());
  }
  return map;
}

// Detect a cycle and compute the longest remaining SUCCESSOR-chain depth for
// each WU (critical-path length) via DFS with memoization. Throws on a cycle.
//
// Depth is measured over the reverse (successor) graph, not the forward
// (deps) graph: a WU only becomes a readiness candidate once all of its own
// deps are COMPLETE, so its ancestor-chain depth is a stale signal at
// ranking time. What matters is how much future/downstream work a WU
// unlocks, i.e. the depth of the chain of WUs that (transitively) depend on
// it.
function criticalPathDepths(wus) {
  const byId = new Map(wus.map((w) => [w.id, w]));
  // successors[id] = WUs that list `id` in their own deps (i.e. depend on it)
  const successors = new Map(wus.map((w) => [w.id, []]));
  for (const w of wus) {
    for (const d of w.deps) {
      if (successors.has(d)) successors.get(d).push(w.id);
    }
  }

  const depth = new Map();
  const VISITING = -1;

  function dfs(id) {
    if (depth.has(id)) {
      const d = depth.get(id);
      if (d === VISITING) throw new Error(`deps cycle detected at ${id}`);
      return d;
    }
    depth.set(id, VISITING);
    let best = 0;
    for (const s of successors.get(id) || []) {
      best = Math.max(best, 1 + dfs(s));
    }
    depth.set(id, best);
    return best;
  }

  for (const w of byId.values()) dfs(w.id);
  return depth; // id -> longest chain of WUs that transitively depend on it
}

function computeReadiness({ wus, statusOf, maxParallel, isolateFileScope = false }) {
  const status = (id) => statusOf(id) || 'PENDING';
  const depths = criticalPathDepths(wus); // also throws on cycle

  const running = wus.filter((w) => status(w.id) === 'IN-PROGRESS').map((w) => w.id);
  const runningWus = wus.filter((w) => running.includes(w.id));

  // occupied = union of every running WU's file_scope
  const occupied = new Set();
  for (const w of runningWus) for (const p of w.fileScope) occupied.add(p);

  const blocked = {};

  // Candidates: PENDING WUs whose deps are all COMPLETE.
  const candidates = wus.filter((w) => {
    if (status(w.id) !== 'PENDING') return false;
    const unmet = w.deps.filter((d) => status(d) !== 'COMPLETE');
    if (unmet.length > 0) {
      blocked[w.id] = { reason: 'deps', detail: `waiting on ${unmet.join(', ')}` };
      return false;
    }
    return true;
  });

  // Critical-path order: longest remaining chain first, ties broken by id.
  candidates.sort((a, b) => (depths.get(b.id) - depths.get(a.id)) || a.id.localeCompare(b.id));

  const ready = [];
  const tentative = new Set(occupied); // atomic all-or-nothing acquisition
  let slots = maxParallel - running.length;

  for (const w of candidates) {
    // Disjoint-file-scope gate. Skipped under worktree isolation: overlapping
    // scopes are admitted concurrently because each WU runs in its own worktree
    // and is merged back with conflict detection. Deps, cycle detection, and the
    // concurrency cap are UNCHANGED in both modes.
    if (!isolateFileScope) {
      const conflict = w.fileScope.find((p) => tentative.has(p));
      if (conflict) {
        blocked[w.id] = { reason: 'file_conflict', detail: `${conflict} overlaps a running/admitted WU` };
        continue;
      }
    }
    if (slots <= 0) {
      blocked[w.id] = { reason: 'concurrency_cap', detail: `max_parallel_wus=${maxParallel} reached` };
      continue;
    }
    ready.push(w.id);
    for (const p of w.fileScope) tentative.add(p); // union whole scope atomically
    slots -= 1;
  }

  return { ready, blocked, running, occupied_files: [...occupied] };
}

function readMaxParallel() {
  const root = process.env.CLAUDE_PROJECT_DIR || process.cwd();
  const thresholdsPath = path.join(root, '.tl-telar-thresholds.json');
  try {
    const cfg = JSON.parse(fs.readFileSync(thresholdsPath, 'utf8'));
    const n = cfg && cfg.execution && cfg.execution.max_parallel_wus;
    if (Number.isInteger(n) && n > 0) return n;
  } catch {
    // absent or malformed thresholds -> default
  }
  return 3;
}

function main(argv) {
  const rest = argv.slice(2);
  // `--isolate` (or TL_TELAR_WORKTREE_ISOLATION=1) relaxes the disjoint-file-scope
  // gate for worktree-isolated WU execution. The orchestrator passes it ONLY after
  // its own capability probe + config check confirm worktree isolation is active
  // (fail-closed). The scheduler stays pure: it honors the flag, never probes.
  const flags = rest.filter((a) => a.startsWith('--'));
  const positionals = rest.filter((a) => !a.startsWith('--'));
  const isolateFileScope =
    flags.includes('--isolate') || process.env.TL_TELAR_WORKTREE_ISOLATION === '1';
  const [planPath, statePath] = positionals;
  if (!planPath || !statePath) {
    console.error('Usage: node scripts/tl-telar-wu-scheduler.js [--isolate] <active-plan.md> <execution-state.md>');
    process.exit(2);
  }
  let planText, stateText;
  try {
    planText = fs.readFileSync(planPath, 'utf8');
    stateText = fs.readFileSync(statePath, 'utf8');
  } catch (err) {
    console.error(`ERROR: cannot read state file: ${err.message}`);
    process.exit(1);
  }

  const { wus, warnings } = parsePlan(planText, isolateFileScope);
  const statusMap = parseState(stateText);
  const statusOf = (id) => statusMap.get(id) || 'PENDING';
  const maxParallel = readMaxParallel();

  let result;
  try {
    result = computeReadiness({ wus, statusOf, maxParallel, isolateFileScope });
  } catch (err) {
    console.error(`ERROR: ${err.message}`);
    process.exit(1);
  }

  process.stdout.write(JSON.stringify({ ...result, plan_warnings: warnings }) + '\n');
}

if (require.main === module) {
  main(process.argv);
}

module.exports = {
  parsePlan, parseState, computePlanWarnings, buildReachability,
  criticalPathDepths, computeReadiness, readMaxParallel, main,
};
