#!/usr/bin/env node

/**
 * Compiles all agent summaries into a single AGENTS.md file.
 * This provides an efficient overview for agent consumption.
 */

const fs = require('fs');
const path = require('path');

const AGENTS_DIR = path.join(__dirname, '..', 'agents');
const OUTPUT_FILE = path.join(__dirname, '..', 'AGENTS.md');

// Strip surrounding quotes ONLY when the entire value is wrapped in a matched
// pair of the same quote char. A naive /^["']|["']$/g (global) wrongly strips a
// trailing quote from plain scalars that merely END with a quote, e.g.
//   User explicitly requests "orchestrated mode" or "full pipeline"
// would lose its final '"'. This matched-pair version leaves such scalars intact.
function stripWrappingQuotes(s) {
  const m = s.match(/^(["'])([\s\S]*)\1$/);
  return m ? m[2] : s;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;

  const yaml = match[1];
  const fields = {};
  let currentKey = null;
  let currentList = null;

  for (const line of yaml.split('\n')) {
    const keyMatch = line.match(/^(\w+):\s*(.*)/);
    if (keyMatch) {
      if (currentKey && currentList) {
        fields[currentKey] = currentList;
      }
      currentKey = keyMatch[1];
      const value = keyMatch[2].trim();

      if (value === '' || value === '|') {
        currentList = [];
      } else if (value.startsWith('[') && value.endsWith(']')) {
        fields[currentKey] = value.slice(1, -1).split(',').map(s => s.trim().replace(/"/g, ''));
        currentKey = null;
        currentList = null;
      } else if (!isNaN(value) && value !== '') {
        fields[currentKey] = Number(value);
        currentKey = null;
        currentList = null;
      } else {
        fields[currentKey] = stripWrappingQuotes(value);
        currentKey = null;
        currentList = null;
      }
    } else if (line.match(/^\s+-\s+/)) {
      if (!currentList) currentList = [];
      currentList.push(stripWrappingQuotes(line.replace(/^\s+-\s+/, '').trim()));
    }
  }
  if (currentKey && currentList) {
    fields[currentKey] = currentList;
  }

  return fields;
}

function getFirstHeading(content) {
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1] : null;
}

function getFirstParagraph(content) {
  // Get first paragraph after the frontmatter and heading
  const afterFrontmatter = content.replace(/^---[\s\S]*?---\n*/, '');
  const afterHeading = afterFrontmatter.replace(/^#[^\n]*\n*/, '');
  const firstPara = afterHeading.match(/^([^\n#].+)/m);
  return firstPara ? firstPara[1].trim() : '';
}

// Main
const agentFiles = fs.readdirSync(AGENTS_DIR)
  .filter(f => f.endsWith('.md') && !f.startsWith('_'))
  .sort();

const agents = [];

for (const file of agentFiles) {
  const content = fs.readFileSync(path.join(AGENTS_DIR, file), 'utf-8');
  const frontmatter = parseFrontmatter(content);
  if (!frontmatter) continue;

  agents.push({
    file,
    id: frontmatter.id || file.replace('.md', ''),
    title: getFirstHeading(content) || frontmatter.id,
    description: getFirstParagraph(content),
    tags: frontmatter.tags || [],
    capabilities: frontmatter.capabilities || [],
    useWhen: frontmatter.useWhen || [],
    hasDecisionFramework: !!frontmatter.decisionFramework ||
      content.includes('## Decision Framework') ||
      content.includes('Decision Tree'),
    hasAntiPatterns: content.includes('Anti-Pattern'),
  });
}

// Generate AGENTS.md
let output = `# Telar - Agent Directory

> Auto-generated by \`scripts/compile-agents-md.js\`. Do not edit manually.
> Last compiled: ${new Date().toISOString().split('T')[0]}

## Summary

- **${agents.length} agents** available
- **${agents.filter(a => a.hasDecisionFramework).length}** with decision frameworks
- **${agents.filter(a => a.hasAntiPatterns).length}** with anti-pattern guidance

## Quick Reference

| Agent | Decision Framework | Anti-Patterns |
|-------|-------------------|---------------|
`;

for (const agent of agents) {
  const df = agent.hasDecisionFramework ? 'Yes' : '-';
  const ap = agent.hasAntiPatterns ? 'Yes' : '-';
  output += `| [${agent.id}](#${agent.id}) | ${df} | ${ap} |\n`;
}

output += '\n## Agent Details\n\n';

for (const agent of agents) {
  output += `### ${agent.id}\n\n`;
  output += `**${agent.title}**\n\n`;
  if (agent.description) {
    output += `${agent.description}\n\n`;
  }
  output += `**File:** \`agents/${agent.file}\`\n\n`;

  if (agent.tags.length > 0) {
    output += `**Tags:** ${agent.tags.join(', ')}\n\n`;
  }

  if (agent.capabilities.length > 0) {
    output += '**Capabilities:**\n';
    for (const cap of agent.capabilities) {
      output += `- ${cap}\n`;
    }
    output += '\n';
  }

  if (agent.useWhen.length > 0) {
    output += '**Use when:**\n';
    for (const uw of agent.useWhen) {
      output += `- ${uw}\n`;
    }
    output += '\n';
  }

  output += '---\n\n';
}

fs.writeFileSync(OUTPUT_FILE, output);
console.log(`\x1b[32mCompiled ${agents.length} agents into AGENTS.md\x1b[0m`);
