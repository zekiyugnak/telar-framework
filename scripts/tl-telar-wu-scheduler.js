#!/usr/bin/env node
'use strict';

const fs = require('fs');

// Parse the `## Work Units` section of active-plan.md into structured WUs.
// Each WU is a `### WU-xxx:` block; within it we read the `file_scope:` and
// `deps:` list/inline fields. Unknown fields (spec, dod, checkpoint) are ignored.
function parsePlan(planText) {
  const lines = planText.split('\n');
  const wus = [];
  let current = null;
  let listKey = null; // 'file_scope' | 'deps' while consuming a multiline list

  const headerRe = /^###\s+(WU-[A-Za-z0-9_-]+):/;
  const inlineDepsRe = /^-\s*deps:\s*\[([^\]]*)\]\s*$/;
  const listStartRe = /^-\s*(file_scope|deps):\s*$/;
  const listItemRe = /^\s+-\s+(.+?)\s*$/;

  const pushCurrent = () => {
    if (current) wus.push(current);
  };

  for (const line of lines) {
    const h = line.match(headerRe);
    if (h) {
      pushCurrent();
      current = { id: h[1], deps: [], fileScope: [] };
      listKey = null;
      continue;
    }
    if (!current) continue;

    const inlineDeps = line.match(inlineDepsRe);
    if (inlineDeps) {
      current.deps = inlineDeps[1]
        .split(',')
        .map((s) => s.trim())
        .filter(Boolean);
      listKey = null;
      continue;
    }

    const ls = line.match(listStartRe);
    if (ls) {
      listKey = ls[1];
      continue;
    }

    if (listKey) {
      const item = line.match(listItemRe);
      if (item) {
        const val = item[1].trim();
        if (listKey === 'file_scope') current.fileScope.push(val);
        else current.deps.push(val);
        continue;
      }
      // A non-indented / non-list line ends the current list.
      listKey = null;
    }
  }
  pushCurrent();

  return { wus, warnings: computePlanWarnings(wus) };
}

// Static conflict check (Bazel "output generated by 2 actions"): two WUs with
// no dep edge between them that both declare the same path is an ambiguous plan.
function computePlanWarnings(wus) {
  const warnings = [];
  const depReachable = buildReachability(wus);
  for (let i = 0; i < wus.length; i++) {
    for (let j = i + 1; j < wus.length; j++) {
      const a = wus[i];
      const b = wus[j];
      const ordered = depReachable(a.id, b.id) || depReachable(b.id, a.id);
      if (ordered) continue;
      const shared = a.fileScope.filter((p) => b.fileScope.includes(p));
      if (shared.length > 0) {
        warnings.push(
          `${a.id} and ${b.id} both declare ${shared.join(', ')} with no dep ordering — ` +
            'ambiguous plan; add a dep edge or split the shared file.'
        );
      }
    }
  }
  return warnings;
}

// Returns a predicate reachable(from, to): is `to` a transitive dependency of `from`?
function buildReachability(wus) {
  const deps = new Map(wus.map((w) => [w.id, w.deps]));
  return function reachable(from, to) {
    const seen = new Set();
    const stack = [...(deps.get(from) || [])];
    while (stack.length) {
      const cur = stack.pop();
      if (cur === to) return true;
      if (seen.has(cur)) continue;
      seen.add(cur);
      stack.push(...(deps.get(cur) || []));
    }
    return false;
  };
}

// Parse the `## Work Unit Status` markdown table into id -> status.
function parseState(stateText) {
  const map = new Map();
  const rowRe = /^\|\s*(WU-[A-Za-z0-9_-]+)\s*\|\s*([A-Za-z-]+)\s*\|/;
  for (const line of stateText.split('\n')) {
    const m = line.match(rowRe);
    if (m) map.set(m[1], m[2].toUpperCase());
  }
  return map;
}

module.exports = { parsePlan, parseState, computePlanWarnings, buildReachability };
